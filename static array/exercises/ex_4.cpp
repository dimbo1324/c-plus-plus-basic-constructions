// Задание №2
// Используя массив из задания №1:
// Попросите пользователя ввести число от 1 до 9. Если пользователь введет что-либо
// другое — попросите его снова ввести число и так до тех пор, пока он не введет
// корректное значение из заданного диапазона. Как только пользователь введет
// число от 1 до 9, выведите массив на экран. Затем найдите в массиве элемент с
// числом, которое ввел пользователь, и выведите его индекс.
// Для обработки некорректного ввода используйте следующий код:
// // Если пользователь ввел некорректное значение
// if (std::cin.fail())
// {
//     std::cin.clear();
//     std::cin.ignore(32767, '\n');
// }
#include <iostream>
#include <array>
#include <windows.h>
int main()
{
    SetConsoleOutputCP(65001);
    SetConsoleCP(65001);
    // Массив из задания №1 — пусть будет какой-то заранее заданный массив из 9 элементов
    std::array<int, 9> arr{3, 1, 4, 1, 5, 9, 2, 6, 5};
    int input = 0;
    // Цикл запроса ввода, пока пользователь не введёт число от 1 до 9
    while (true)
    {
        std::cout << "Введите число от 1 до 9: ";
        std::cin >> input;
        if (std::cin.fail())
        {
            std::cin.clear();             // сброс флага ошибки
            std::cin.ignore(32767, '\n'); // очистка буфера ввода
            std::cout << "Ошибка ввода. Попробуйте снова.\n";
        }
        else if (input < 1 || input > 9)
        {
            std::cout << "Число вне диапазона. Попробуйте снова.\n";
        }
        else
        {
            break; // корректное число
        }
    }
    // Вывод массива
    std::cout << "Массив: ";
    for (int value : arr)
    {
        std::cout << value << ' ';
    }
    std::cout << '\n';
    // Поиск индекса первого элемента, равного введённому числу
    bool found = false;
    for (std::size_t i = 0; i < arr.size(); ++i)
    {
        if (arr[i] == input)
        {
            std::cout << "Индекс первого вхождения числа " << input << " в массив: " << i << '\n';
            found = true;
            break;
        }
    }
    if (!found)
    {
        std::cout << "Число " << input << " не найдено в массиве.\n";
    }
    return 0;
}
