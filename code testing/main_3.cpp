#include <iostream>
#include <windows.h>
/*=====================================================*/
/*=====================================================*/
constexpr unsigned long long factorial(unsigned n) {
    return (n <= 1) ? 1ULL : (n * factorial(n - 1));
}

static_assert(factorial(5) == 120ULL, "5! must be 120");
static_assert(factorial(0) == 1ULL, "0! must be 1");
/*=====================================================*/
/*=====================================================*/
int main() {
    SetConsoleOutputCP(65001);
    SetConsoleCP(65001);
    /*=====================================================*/
    /*=====================================================*/
    constexpr auto f6 = factorial(6);
    std::cout << "6! = " << f6 << '\n';
    /*=====================================================*/
    /*=====================================================*/
    return 0;
}
