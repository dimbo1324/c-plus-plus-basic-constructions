/*
Функция add с ограничением параметров через концепт Addable
*/
#include <iostream>
#include <concepts>
#include <windows.h>
/*=====================================================*/
/*=====================================================*/
template <typename T>
concept Addable = requires(T a, T b) {
    { a + b } -> std::convertible_to<T>;
};
/*=====================================================*/
/*=====================================================*/
template <Addable T>
T add(T a, T b)
{
    return a + b;
}
/*=====================================================*/
/*=====================================================*/
int main()
{
    SetConsoleOutputCP(65001);
    SetConsoleCP(65001);
    /*=====================================================*/
    /*=====================================================*/
    std::cout << "add(7,5) = " << add(7, 5) << '\n';
    std::cout << "add(2.5,1.5) = " << add(2.5, 1.5) << '\n';
    // add(std::string("a"), 1); // понятная ошибка компиляции
    /*=====================================================*/
    /*=====================================================*/
    return 0;
}
