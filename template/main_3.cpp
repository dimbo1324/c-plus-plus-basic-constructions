/*
Пример alias-шаблона для удобного имени умного указателя
*/
#include <iostream>
#include <memory>
#include <windows.h>

/*=====================================================*/
/*=====================================================*/
template <typename T>
using Ptr = std::unique_ptr<T>;

/*=====================================================*/
/*=====================================================*/
int main()
{
    SetConsoleOutputCP(65001);
    SetConsoleCP(65001);
    /*=====================================================*/
    /*=====================================================*/
    Ptr<int> p = std::make_unique<int>(42);
    std::cout << "*p = " << *p << '\n';
    /*=====================================================*/
    /*=====================================================*/
    return 0;
}
