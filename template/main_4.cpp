/*
Пример variable-шаблона для математической константы π
*/
#include <iostream>
#include <windows.h>

/*=====================================================*/
/*=====================================================*/
template <typename T>
constexpr T pi = T(3.1415926535897932385L);
/*=====================================================*/
/*=====================================================*/
int main()
{
    SetConsoleOutputCP(65001);
    SetConsoleCP(65001);
    /*=====================================================*/
    /*=====================================================*/
    std::cout << "pi<int>   = " << pi<int> << '\n';
    std::cout << "pi<double> = " << pi<double> << '\n';
    /*=====================================================*/
    /*=====================================================*/
    return 0;
}
