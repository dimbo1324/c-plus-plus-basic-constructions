/*
Пример шаблона для вычисления суммы ряда аргументов
*/
#include <iostream>
#include <windows.h>
/*=====================================================*/
/*=====================================================*/
template <typename T, T... Ns>
constexpr T sum_pack()
{
    return (0 + ... + Ns); // fold-expression (C++17)
}
/*=====================================================*/
/*=====================================================*/
int main()
{
    SetConsoleOutputCP(65001);
    SetConsoleCP(65001);
    /*=====================================================*/
    /*=====================================================*/
    constexpr auto s1 = sum_pack<int, 1, 2, 3, 4>();
    constexpr auto s2 = sum_pack<int, 10, 20, 30>();
    std::cout << "sum_pack<1,2,3,4> = " << s1 << '\n';
    std::cout << "sum_pack<10,20,30> = " << s2 << '\n';
    /*=====================================================*/
    /*=====================================================*/
    return 0;
}
