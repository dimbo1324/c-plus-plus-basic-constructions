/*
Пример класс-шаблона для простого контейнера «Пара»
*/
#include <iostream>
#include <windows.h>
/*=====================================================*/
/*=====================================================*/
template <typename T1, typename T2>
class Pair
{
public:
    T1 first;
    T2 second;
    Pair(T1 a, T2 b) : first(a), second(b) {}
};
/*=====================================================*/
/*=====================================================*/
int main()
{
    SetConsoleOutputCP(65001);
    SetConsoleCP(65001);
    /*=====================================================*/
    /*=====================================================*/
    Pair<int, std::string> p{1, "один"};
    std::cout << "Pair: {" << p.first << ", " << p.second << "}\n";
    /*=====================================================*/
    /*=====================================================*/
    return 0;
}
