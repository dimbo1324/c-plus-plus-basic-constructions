/*
Пример функции-шаблона для вычисления максимума двух значений
*/
#include <iostream>
#include <windows.h>
/*=====================================================*/
/*=====================================================*/
template <typename T>
T my_max(T a, T b)
{
    return (a > b) ? a : b;
}
/*=====================================================*/
/*=====================================================*/
int main()
{
    SetConsoleOutputCP(65001);
    SetConsoleCP(65001);
    /*=====================================================*/
    /*=====================================================*/
    std::cout << "Максимальное число: my_max(3, 7) = " << my_max(3, 7) << '\n';
    std::cout << "Максимальное число: my_max(3.5, 2.1) = " << my_max(3.5, 2.1) << '\n';
    /*=====================================================*/
    /*=====================================================*/
    return 0;
}
